# One data type to another data type 

num1 = input("Enter 1st number: ") #input will always in string data type
num2 = input("Enter 2nd number: ")

result = int(num1) + float(num2)

print(result)

result = int(num1) - float(num2)
print(result)

num3 = int(input("Enter 3rd number: "))

result = result * num3

print (result)